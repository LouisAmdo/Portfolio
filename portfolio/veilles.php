<center><h1>Qu'est-ce-qu'une Veille ?</h1></center>

<h3>Une veille est une activité consistant à rester au courant des dernières avancées et informations sur un secteur donné.
Durant mon Bachelor numérique, j’ai personnellement décidé de me mettre à jour sur 2 Veilles : <br><br>

   - Une Veille Technologique, qui elle, sert à rester informé sur les différentes innovations et technologies disponibles sur un marché.
    Mais aussi à la Cybersécurité qui devient de plus en plus importante du fait des objets de plus en plus présents connectés au réseau (IoT) <br><br>

   - Une Veille Juridique, qui elle, sert à rester informé sur les lois ainsi que toutes les informations dans le domaine juridique de l’informatique <br><br>

Une veille est très importante car il faut être à l’affût des dernières nouveautées, et l’informatique étant un domaine qui évolue sans cesse.
</h3> <br><br><br>


